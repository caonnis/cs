function toggleLanguageMenu(event) {
    event.stopPropagation();
    const menu = document.querySelector('.language-selector');
    const overlay = document.querySelector('.language-overlay');
    const isActive = menu.classList.contains('active');
    
    if (isActive) {
        closeLanguageMenu();
    } else {
        openLanguageMenu();
    }
}

function openLanguageMenu() {
    const menu = document.querySelector('.language-selector');
    const overlay = document.querySelector('.language-overlay');
    
    menu.classList.add('active');
    if (window.innerWidth <= 768) {
        overlay.classList.add('active');
    }
    
    // Agregar listeners para cerrar
    document.addEventListener('click', closeOnClickOutside);
    if (window.innerWidth <= 768) {
        overlay.addEventListener('click', closeLanguageMenu);
    }
}

function closeLanguageMenu() {
    const menu = document.querySelector('.language-selector');
    const overlay = document.querySelector('.language-overlay');
    
    menu.classList.remove('active');
    if (overlay) overlay.classList.remove('active');
    
    // Remover listeners
    document.removeEventListener('click', closeOnClickOutside);
    if (overlay) overlay.removeEventListener('click', closeLanguageMenu);
}

function closeOnClickOutside(event) {
    const menu = document.querySelector('.language-selector');
    const globeIcon = document.querySelector('.globe-icon');
    
    if (!menu.contains(event.target) && !globeIcon.contains(event.target)) {
        closeLanguageMenu();
    }
}

